package com.napstablook.common.mainCategories.achievements;

public class Achievements
{
    private General general;

    public General getGeneral() { return this.general; }

    public void setGeneral(General general) { this.general = general; }

    private Defense defense;

    public Defense getDefense() { return this.defense; }

    public void setDefense(Defense defense) { this.defense = defense; }

    private Offense offense;

    public Offense getOffense() { return this.offense; }

    public void setOffense(Offense offense) { this.offense = offense; }

    private Tank tank;

    public Tank getTank() { return this.tank; }

    public void setTank(Tank tank) { this.tank = tank; }

    private Support support;

    public Support getSupport() { return this.support; }

    public void setSupport(Support support) { this.support = support; }

    private Maps maps;

    public Maps getMaps() { return this.maps; }

    public void setMaps(Maps maps) { this.maps = maps; }

    private Special special;

    public Special getSpecial() { return this.special; }

    public void setSpecial(Special special) { this.special = special; }
}

